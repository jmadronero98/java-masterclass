public class a11_operatorsOperandsAndExpressions {
    public static void main(String[] args){
        int result = 1 + 2;
        System.out.println("1 + 2 = " + result); //1 + 2 = 3

        int secondResult = 2 - 1;
        System.out.println("2 - 1 = " + secondResult); //2 - 1 = 1

        int thirdResult = 2 * 1;
        System.out.println("2 * 1 = " + thirdResult); //2 * 1 = 2

        int fourthResult = 2 / 1;
        System.out.println("2 / 1 = " + fourthResult); //2 / 1 = 2

        int fifthResult = 2 % 3;
        System.out.println("2 % 3 = " + fifthResult); //2 % 1 = 2
    }
}
